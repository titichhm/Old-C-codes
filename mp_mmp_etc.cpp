//associative containers
#include <bits/stdc++.h>

using namespace std;

int main()
{
	int t,n;
	cin>>t;
	for(int i=0;i<t;i++)
	{
	   cin>>n;
	   if(n<=250000)
	   {
	   		cout<<n<<endl;
	   }
	   else if(n>=250001 and n<=500000)
	   {
	   		int x=(n-2500000)*0.05;
	   		cout<<(n-x)<<endl;
	   }
	   else if(n>=500001 and n<=750000)
	   {
	   		int x=(500000-250000)*0.05+(n-500000)*0.1;
	   		cout<<(n-x)<<endl;
	   }
	   else if(n>=750001 and n<=1000000)
	   {
	   		int x=(500000-250000)*0.05+(750000-500000)*0.1+(n-750000)*0.15;
	   		cout<<(n-x)<<endl;
	   }
	   else if(n>= 1000001 and n<=1250000)
	   {
	   		int x=(500000-250000)*0.05+(750000-500000)*0.1+(1000000-750000)*0.15+(n-1000000)*0.2;
	   		cout<<(n-x)<<endl;
	   }
	   else if(n>= 1250001 and n<=1500000)
	   {
	   		int x=(500000-250000)*0.05+(750000-500000)*0.1+(1000000-750000)*0.15+(1250000-1000000)*0.2+(n-1250000)*0.25;
	   		cout<<(n-x)<<endl;
	   }
	   else if(n>1500000)
	   {
	   		int x=(500000-250000)*0.05+(750000-500000)*0.1+(1000000-750000)*0.15+(1250000-1000000)*0.2+(1500000-1250000)*0.25+(n-1500000)*0.3;
	   		cout<<(n-x)<<endl;
	   }
	}
	
	return 0;
}

